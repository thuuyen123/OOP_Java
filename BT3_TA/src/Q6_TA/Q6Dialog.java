    private boolean isBeautifulNumber(String number){
        StringBuilder st = new StringBuilder(number);
        return st.reverse().toString().equals(number);
    }
    
    private String setText(String text){
        String fullName = p.getFullName();
        String[] splits = fullName.split(" ");
        String mail = splits[splits.length-1];
        
        for (int i = 0; i < splits.length-1; i++) {
            mail += Character.toUpperCase(splits[i].charAt(0));
        }
        
        if(jCheckBox1.isSelected()){
            text += "contact to: " + mail + "@ptit.edu.vn";
        }
        return text;
    }
    
