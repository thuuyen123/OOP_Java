    public Sach getObject(){
        return new Sach(tm.getValueAt(getEditRow(),0).toString(), 
                tm.getValueAt(getEditRow(),1).toString(),
                Double.parseDouble(tm.getValueAt(getEditRow(),2).toString()));
    }
    
    public void setObject(Sach s){
        tm.setValueAt(s.getMa(), getEditRow(), 0);
        tm.setValueAt(s.getTen(), getEditRow(), 1);
        tm.setValueAt(s.getGia(), getEditRow(), 2);
    }
    
    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        // Sua
        int row = jTable1.getSelectedRow();
        if(row < 0 || row >= tm.getRowCount()){
            JOptionPane.showMessageDialog(null, "Chon dong de sua");
            return;
        } else {
            setEditRow(row);
            EditForm f = new EditForm(this, true);
            f.setVisible(true);
        }
    }//GEN-LAST:event_jButton2ActionPerformed

