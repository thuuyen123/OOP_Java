    private boolean isSachExisted(String id){
        //kiem tra xem ma co trung hay khong
        for (int i = 0; i < tm.getRowCount(); i++) {
            if(tm.getValueAt(i, 0).toString().equalsIgnoreCase(id)){
                return true;
            }
        }
        return false;
    }
    
    public void add(Sach s){
        if(isSachExisted(s.getMa())){
            JOptionPane.showMessageDialog(null, "Ma da ton tai");
            return;
        }
        else tm.addRow(s.toObject());
    }
    
