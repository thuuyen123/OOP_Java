    private List<MatHang> getAllMatHang(){
        List<MatHang> a = new ArrayList<>();
        
        for (int i = 0; i < tmM.getRowCount(); i++) {
            MatHang m = new MatHang(Integer.parseInt(tmM.getValueAt(i, 0).toString()),
                    tmM.getValueAt(i, 1).toString(),
                    tmM.getValueAt(i, 2).toString(), 
                    Double.parseDouble(tmM.getValueAt(i, 3).toString()));
            a.add(m);
        }
        return a;
    }
    private void hienThibangMH(List<MatHang> a){
        DefaultTableModel tm = (DefaultTableModel)jTable1.getModel();
        tm.setRowCount(0);
        for (MatHang i : a) {
            int ma = i.getMa();
            String ten = i.getTen();
            String nhom = i.getNhom();
            double gia = i.getGia();
            MatHang m = new MatHang(ma, ten, nhom, gia);
            tm.addRow(m.toObject());
        }
    }
