    private void loadMH(){
        File file = new File(fileM);
        if(file.exists()){
            List<MatHang> a = new ArrayList<>();
            IOFile.readFile(a, fileM);
            tmM.setRowCount(0);
            for (MatHang i : a) {
                tmM.addRow(i.toObject());
            }
        }
    }
    private void loadKH(){
        File file = new File(fileK);
        if(file.exists()){
            List<KhachHang> a = new ArrayList<>();
            IOFile.readFile(a, fileK);
            tmK.setRowCount(0);
            for (KhachHang i : a) {
                tmK.addRow(i.tObject());
            }
        }
    }
    private MatHang getMHbyMa(int ma){
        
        for (int i = 0; i < tmM.getRowCount(); i++) {
            if(Integer.parseInt(tmM.getValueAt(i, 0).toString())==ma){
                return new MatHang(ma, 
                        tmM.getValueAt(i, 1).toString(), 
                        tmM.getValueAt(i, 2).toString(),
                        Double.parseDouble(tmM.getValueAt(i, 3).toString()));
            }
            
        }
        return null;
    }
    private KhachHang getKHbyMa(int ma){
        for (int i = 0; i < tmK.getRowCount(); i++) {
           if(Integer.parseInt(tmK.getValueAt(i, 0).toString())==ma){
               return new KhachHang(ma, tmK.getValueAt(i, 1).toString(), 
                       tmK.getValueAt(i, 2).toString(), 
                       tmK.getValueAt(i, 3).toString()
               );
           }
        }
        return null;
    }
