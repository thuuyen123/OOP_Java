    //ham kiem tra nguyen to.
    public boolean ngTo(int a) {
        if(a<2) return false;
        if(a==2)    return true;
        for(int i=2;i<=Math.sqrt(a);i++) {
            if(a%i==0)  return false;
        }
        return true;
    }
    //ham kiem tra chan le, tra ve 1 neu chan va 0 neu le.
    public int chanLe(int a) {
        if(a%2==0) return 1;
        else   return 0;
    }
    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        String s="";
        s+=m.getTextArea()+"\n";
        String[] nums=m.getTextField().split("\\s+");
        if(jRadioButton1.isSelected()) {
            s+="Day so nguyen to: ";
            for(int i=0;i<nums.length;i++) {
                if(ngTo(Integer.parseInt(nums[i]))) {
                    s+=nums[i]+" ";
                }
            }
        }
        else if(jRadioButton2.isSelected()) {
            s+="Day so chan: ";
            for(int i=0;i<nums.length;i++) {
                if(chanLe(Integer.parseInt(nums[i]))==1) {
                    s+=nums[i]+" ";
                }
            }
        }
        else{
           s+="Day so le: ";
            for(int i=0;i<nums.length;i++) {
                if(chanLe(Integer.parseInt(nums[i]))==0) {
                    s+=nums[i]+" ";
                }
            } 
        }
        s+="\n";
        if(jCheckBox1.isSelected()) {
            s+="Duoc thuc hien boi ";
            List<String> ten = jList1.getSelectedValuesList();
            for(String i:ten) {
                s+=i+", ";
            }
            s=s.substring(0, s.length()-2)+"\n";
        }
        m.setTextArea(s);
    }//GEN-LAST:event_jButton1ActionPerformed

