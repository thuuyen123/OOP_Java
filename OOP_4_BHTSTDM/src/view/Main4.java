    private int getMaMH(){
        return 1000+tmM.getRowCount();
    }
    private int getMaNV(){
        return 1000+tmN.getRowCount();
    }
    private MatHang getMHbyMa(int ma){
        for (int i = 0; i < tmM.getRowCount(); i++) {
            if(Integer.parseInt(tmM.getValueAt(i, 0).toString())==ma){
                return new MatHang(ma,
                        tmM.getValueAt(i, 1).toString(),
                        tmM.getValueAt(i, 2).toString(),
                        Double.parseDouble(tmM.getValueAt(i, 3).toString()),
                        Integer.parseInt(tmM.getValueAt(i, 4).toString()));
            }
            
        }
        return null;
    }
    private NhanVien getNVbyMa(int ma){
        for (int i = 0; i < tmN.getRowCount(); i++) {
            if(Integer.parseInt(tmN.getValueAt(i, 0).toString())==ma){
                return new NhanVien(ma,
                        tmN.getValueAt(i, 1).toString(),
                        tmN.getValueAt(i, 2).toString(),
                        tmN.getValueAt(i, 3).toString());
            }
            
        }
        return null;
    }
    private void loadM(){
        File file = new File(fileM);
        if(file.exists()){
            List<MatHang> a = new ArrayList<>();
            IOFile.readFile(a, fileM);
            tmM.setRowCount(0);
            for (MatHang i : a) {
                tmM.addRow(i.toObjects());
            }
        }
    }
    private void loadN(){
        File file = new File(fileN);
        if(file.exists()){
            List<NhanVien> a = new ArrayList<>();
            IOFile.readFile(a, fileN);
            tmN.setRowCount(0);
            for (NhanVien i : a) {
                tmN.addRow(i.toObjects());
            }
        }
    }
