
package ngay21_8;


public class StatementDemo {
    public static void main(String[] args) {
        int[] a={7,56,45,333};
        for (int i = 0; i < a.length; i++) {
            System.out.println(a[i]);
        }
        for(int x:a)
            System.out.println(x);
        String[] ds={"to lan a","le thi tao","hhh hhh hh"};
        for(String i:ds)
            System.out.println(i);
        
    }
    
}
