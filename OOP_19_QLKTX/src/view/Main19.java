    private List<Phong> search(String loai){
        List<Phong> a = getAllByPhong();
        List<Phong> b = new ArrayList<>();
        for (int i = 0; i < a.size(); i++) {
           if(a.get(i).getLoaip().toUpperCase().contains(loai.toUpperCase())){
               b.add(a.get(i));
           }
            
        }
        return b;
    }
