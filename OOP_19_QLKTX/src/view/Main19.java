    private int getMaSv(){
        return 10000+tmS.getRowCount();
    }
    private int getMaPhong(){
        return 100+tmP.getRowCount();
    }
    private void loadSV(){
        File file = new File(fileS);
        if(file.exists()){
            List<SinhVien> a= new ArrayList<>();
            IOFile.readFile(a, fileS);
            tmS.setRowCount(0);
            for (SinhVien i : a) {
                tmS.addRow(i.toObjects());
            }
        }
    }
    private void loadPhong(){
        File file = new File(fileP);
        if(file.exists()){
            List<Phong> a= new ArrayList<>();
            IOFile.readFile(a, fileP);
            tmP.setRowCount(0);
            for (Phong i : a) {
                tmP.addRow(i.toObjects());
            }
        }
    }
    
    private void fileName(){
        fileS= "src/controller/SV.DAT";
        fileP= "src/controller/PHONG.DAT";
        
    }
    private SinhVien getSVbyMa(int ma){
        for (int i = 0; i < tmS.getRowCount(); i++) {
            if(tmS.getValueAt(i, 0).toString().equals(ma)){
                return new SinhVien(ma, 
                        tmS.getValueAt(i, 1).toString(), 
                        tmS.getValueAt(i, 2).toString(), 
                        tmS.getValueAt(i, 3).toString(), 
                        tmS.getValueAt(i, 4).toString());
            }
            
        }
        return null;
    }
    private Phong getPhongByMa(int ma){
        for (int i = 0; i < tmP.getRowCount(); i++) {
            if(tmP.getValueAt(i, 0).toString().equals(ma)){
                return new Phong(ma, 
                        tmS.getValueAt(i, 1).toString(), 
                        tmS.getValueAt(i, 2).toString());
                        
            }
            
        }
        return null;
    }

