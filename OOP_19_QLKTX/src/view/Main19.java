    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
       int ma = Integer.parseInt(jTextField1.getText());
       String ten = jTextField2.getText();
       String diachi = jTextField3.getText();
       String ngaysinh = jTextField4.getText();
       String lop = jTextField5.getText();
       if(!lop.matches("^d{2}\\[/]\\d{2}\\[/]\\d{4}" )){
           JOptionPane.showConfirmDialog(null, "Lop phai dung dinh dang");
           return;
       }
       else{
           SinhVien s = new SinhVien(ma, ten, diachi, ngaysinh, lop);
           if(themS){
               tmS.addRow(s.toObjects());
               themS=false;
           }
       }
    }//GEN-LAST:event_jButton2ActionPerformed

