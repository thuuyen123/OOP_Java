    private List<SinhVien> getAllBySV(){
        List<SinhVien> a = new ArrayList<>();
        for (int i = 0; i < tmS.getRowCount(); i++) {
            SinhVien s = new SinhVien(Integer.parseInt(tmS.getValueAt(i, 0).toString()), 
                        tmS.getValueAt(i, 1).toString(), 
                        tmS.getValueAt(i, 2).toString(), 
                        tmS.getValueAt(i, 3).toString(), 
                        tmS.getValueAt(i, 4).toString());
            a.add(s);
        }
        return a;
    }
    private List<Phong> getAllByPhong(){
        List<Phong> a = new ArrayList<>();
        for (int i = 0; i < tmP.getRowCount(); i++) {
            Phong p = new Phong(Integer.parseInt(tmP.getValueAt(i, 0).toString()), 
                        tmP.getValueAt(i, 1).toString(), 
                        tmP.getValueAt(i, 2).toString());
                        
            a.add(p);
        }
        return a;
    }
    private void hienThiBangSV(List<SinhVien> a){
        String[] cols = {"Ma sv","Ten sv","Dia chi","Ngay sinh", "Lop"};
        DefaultTableModel tm = new DefaultTableModel(cols, 0);
        jTable1.setModel(tm);
        tm.setRowCount(0);
        for (SinhVien i : a) {
            int ma = i.getMa();
            String ten = i.getHoten();
            String diac = i.getDiac();
            String ngsinh = i.getNgsinh();
            String lop = i.getLop();
            SinhVien s = new SinhVien(ma, ten, diac, ngsinh, lop);
            tm.addRow(s.toObjects());
            
        }
      
    }
    private void hienthiBangPhong(List<Phong> a){
        String[] cols1 = {"Ma phong", "Ten phong","Loai phong"};
        DefaultTableModel tm1 = new DefaultTableModel(cols1,0);
        jTable2.setModel(tm1);
        tm1.setRowCount(0);
        for (Phong i : a) {
            int ma = i.getMap();
            String ten = i.getTenp();
            String loai = i.getLoaip();
            Phong p = new Phong(ma, ten, loai);
            tm1.addRow(p.toObjects());
        }
    }
