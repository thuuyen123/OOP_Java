    public boolean checkNgto(int n){
        if(n<2) return false;
        for (int i = 2; i < Math.sqrt(n); i++) {
            if(n%i==0) return false;            
        }
        return true;
    }
    public boolean checkChan(int n){
       if(n%2==0) return true;
       return false;
    }
    public boolean checkLe(int n){
        if(n%2!=0) return true;
        return false;
    }
    
