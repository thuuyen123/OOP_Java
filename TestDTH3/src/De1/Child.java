    public boolean isNguyenTo(int n){
        if(n<2) return false;
        for (int i = 2; i <= sqrt(n); i++) {
            if(n%i==0) return false;
            
        }
        return true;
    }
    public boolean isChan(int n){
        if(n%2==0) return true;
        return false;
    }
    public boolean isLe(int n){
        if(n%2==0) return false;
        return true;
    }
    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        String content="Day so ";
        String num=p.getSo();
        String [] n=num.split("\\s+");
        int []a=new int[n.length];
        for (int i = 0; i < n.length; i++) {
            a[i]=Integer.parseInt(n[i]);
        }
        if(jRadioButton1.isSelected()){
            content+="nguyen to: ";
            for (int i = 0; i < n.length; i++) {
                if(isNguyenTo(a[i])){
                    content+=a[i]+" "; 
                }
            }
        }
        if(jRadioButton2.isSelected()){
            content+="le: ";
            for (int i = 0; i < n.length; i++) {
                if(isLe(a[i])){
                    content+=a[i]+" "; 
                }
            }
        }
        if(jRadioButton3.isSelected()){
            content+="chan: ";
            for (int i = 0; i < n.length; i++) {
                if(isChan(a[i])){
                    content+=a[i]+" ";
                }
                
            }
        }
        if(!jCheckBox1.isSelected()){// chi chon 1
            content+="\nThuc hien boi: ";
            List<String> list=jList1.getSelectedValuesList();
            for (String i : list) {
                content+=i;
            }
        }
        else{//chon nhieu list
            content+="\nThuc hien boi: ";
            int size=jList1.getModel().getSize();
            String[] arr=new String[size];
            for (int i = 0; i < size; i++) {
                arr[i]=jList1.getModel().getElementAt(i);
                content+=arr[i]+"\n";
            }
        }
        p.setContent(content);
    }//GEN-LAST:event_jButton1ActionPerformed

