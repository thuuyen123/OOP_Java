    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
       jButton1.setEnabled(false);
       new Thread(){
           public void run(){
               boolean running = true;
               while(running){
                   try {
                        if(jTextField1.getText().equals(jTextField2.getText())){
                            jTextArea1.append("Good morning at time: "+ jTextField1.getText()+ "\r\n");
                            running = false;
                        }
                        Thread.sleep(100);
                   } catch (InterruptedException ex) {
                       System.out.println(ex);
                   }
                }
               int times = jComboBox1.getSelectedIndex();
               String[] t = jTextField3.getText().split(":");
               long lap = (Long.parseLong(t[0])*60 + Long.parseLong(t[1]))*1000;
               for (int i = 0; i < times; i++) {
                   try {
                       Thread.sleep(lap);
                   } catch (InterruptedException ex) {
                       System.out.println(ex);
                   }
                   jTextArea1.append("Good morning at time: "+ jTextField1.getText()+ "\r\n");
               }
               jButton1.setEnabled(true);
           }
       }.start();
    }//GEN-LAST:event_jButton1ActionPerformed

