    class ChayChu extends Thread {

        @Override
        public void run() {

            while (true) {
                String text = jTextArea1.getText();
                String[] s = text.split("\n");
                text = "";
                for (int i = 1; i < s.length; i++) {
                    text += s[i] + "\n";
                }
                text += s[0];
                jTextArea1.setText(text);
                try {
                    sleep(400);
                } catch (InterruptedException e) {
                    System.out.println(e);
                }
            }
        }
    }

