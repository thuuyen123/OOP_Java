    class ChayChu extends Thread{
        public void run(){
            while (true) {
                String t=jTextArea1.getText();
                String []s=t.split("\n");
                t="";
                for (int i = 1; i < s.length; i++) {
                    t+=s[i]+"\n";
                }
                t+=s[0]+"\n";
                jTextArea1.setText(t);
                try {
                    sleep(500);
                } catch (Exception e) {
                }
                
            }
        }
    }
