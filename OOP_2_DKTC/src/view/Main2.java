    private List<MonHoc> getAllMonHoc(){
        List<MonHoc> a = new ArrayList<>(); 
        for (int i = 0; i < tmMH.getRowCount(); i++) {
           MonHoc m = new MonHoc(Integer.parseInt(tmMH.getValueAt(i, 0).toString()),
                   tmMH.getValueAt(i, 1).toString(),
                   Integer.parseInt(tmMH.getValueAt(i, 2).toString()),
                   tmMH.getValueAt(i, 3).toString());
           a.add(m);
        }
        return a;
    }
    private void hienthiBangMonHoc(List<MonHoc> a){
        DefaultTableModel tm;
        String[] cols1 = {"Ma mon", "Ten mon", "Tong so tiet", "Loai mon hoc"};
        tm = new DefaultTableModel(cols1, 0);
        jTable1.setModel(tm);
        tm.setRowCount(0);
        for (MonHoc i : a) {
            int ma = i.getMaM();
            String ten = i.getTenM();
            int tong = i.getTongST();
            String loai = i.getLoaiM();
            MonHoc m = new MonHoc(ma, ten, tong, loai);
            tm.addRow(m.toObject());
        }
        
    }
    private List<MonHoc> search(int tongTiet){
        List<MonHoc> a = getAllMonHoc();
        List<MonHoc> b = new ArrayList<>();
        for (int i = 0; i < a.size(); i++) {
            if(a.get(i).getTongST() == tongTiet){
                b.add(a.get(i));
            }
       }
        return b;
    }
    private void jButton17ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton17ActionPerformed
        List<MonHoc> a = getAllMonHoc();
        Collections.sort(a, Comparator.comparing( MonHoc :: getTenM));
        hienthiBangMonHoc(a);
    }//GEN-LAST:event_jButton17ActionPerformed

