    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        List<Sach> list=new ArrayList<>();
        for (int i = 0; i < tm.getRowCount(); i++) {
            Sach s=new Sach(tm.getValueAt(i, 0).toString(),
                    tm.getValueAt(i,1).toString(),
                    Integer.parseInt(tm.getValueAt(i,2).toString()),
                    Double.parseDouble(tm.getValueAt(i,3).toString()));
            list.add(s);
        }
        IOFile.vietFile(list, filename);
    }//GEN-LAST:event_jButton2ActionPerformed

    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        List<Sach> a=new ArrayList<>();
        IOFile.docFile(a, filename);
        tm.setRowCount(0);
        for (int i = 0; i < a.size(); i++) {
            tm.addRow(a.get(i).toObject());
        }
    }//GEN-LAST:event_jButton3ActionPerformed

