    class Blinking extends Thread {//nhap nhay xanh do
        public void run(){
            boolean red=true;
            while(true){
                if(red){
                    jLabel1.setForeground(Color.BLUE);
                    red=false;
                }
                else{
                    jLabel1.setForeground(Color.red);
                    red=true;
                }
                try{
                    sleep(500);
                }catch(InterruptedException e){
                    System.out.println(e);
                }
            }
        }
    }
