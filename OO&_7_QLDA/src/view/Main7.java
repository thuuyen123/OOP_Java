    private List<DuAn> getAllDA(){
        List<DuAn>  a = new ArrayList<>();
        for (int i = 0; i < tmD.getRowCount(); i++) {
            DuAn d = new DuAn(Integer.parseInt(tmD.getValueAt(i, 0).toString()),
                        tmD.getValueAt(i, 1).toString(),
                        tmD.getValueAt(i, 2).toString(), 
                        Double.parseDouble(tmD.getValueAt(i, 3).toString())
                );
            a.add(d);
        }
        return a;
    }
    private void hienthiBangDA(List<DuAn> a){
        jTable1.setModel(tmD);
        tmD.setRowCount(0);
        for (DuAn i : a) {
            int ma = i.getMa();
            String ten = i.getTen();
            String kieu = i.getKieu();
            double kp = i.getKphi();
            DuAn d = new DuAn(ma, ten, kieu, kp);
            tmD.addRow(d.toObjects());
        }
    }
    private void jButton11ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton11ActionPerformed
        List<DuAn> a = getAllDA();
        Collections.sort(a, Comparator.comparing(DuAn ::getKieu));
        hienthiBangDA(a);
    }//GEN-LAST:event_jButton11ActionPerformed

