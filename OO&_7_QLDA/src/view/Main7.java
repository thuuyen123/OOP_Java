    private void loadDA(){
        File f = new File(fileD);
        if(f.exists()){
            List<DuAn> a =new ArrayList<>();
            IOFile.readFile(a, fileD);
            tmD.setNumRows(0);
            for (DuAn i : a) {
                tmD.addRow(i.toObjects());
            }
        }
    }
    private void loadNV(){
        File f = new File(fileN);
        if(f.exists()){
            List<NhanVien> a =new ArrayList<>();
            IOFile.readFile(a, fileN);
            tmN.setNumRows(0);
            for (NhanVien i : a) {
                tmN.addRow(i.toObjects());
            }
        }
    }
