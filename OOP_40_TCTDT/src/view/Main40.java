    private void jButton7ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton7ActionPerformed
        int ma = Integer.parseInt(jTextField5.getText());
        String ten= jTextField6.getText();
        String diac= jTextField7.getText();
        String sdt = jTextField8.getText();
        if(!sdt.matches("\\d+")){
            JOptionPane.showMessageDialog(null, "SDt phai full so");
            return;
        }else{
            int num = Integer.parseInt(sdt);
            NhanVien n = new NhanVien(ma, ten, diac, num);
            if(themN){
                tmN.addRow(n.toObjects());
                themN = false;
            }
        }
    }//GEN-LAST:event_jButton7ActionPerformed

