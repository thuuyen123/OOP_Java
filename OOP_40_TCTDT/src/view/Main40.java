    private DichVu getDVbyMa(int ma){
        for (int i = 0; i < tmD.getRowCount(); i++) {
            if(Integer.parseInt(tmD.getValueAt(i, 0).toString())==ma){
                return new DichVu(ma, 
                        tmD.getValueAt(i, 1).toString(),
                        tmD.getValueAt(i, 2).toString(),
                        Double.parseDouble(tmD.getValueAt(i, 3).toString()),
                        Double.parseDouble(tmD.getValueAt(i, 4).toString())
                );
            }
            
        }
        return null;
    }
    
    private NhanVien getNVbyMa(int ma){
        for (int i = 0; i < tmN.getRowCount(); i++) {
            if(Integer.parseInt(tmN.getValueAt(i, 0).toString())==ma){
                return new NhanVien(ma, 
                        tmN.getValueAt(i, 1).toString(),
                        tmN.getValueAt(i, 2).toString(),
                        Integer.parseInt(tmN.getValueAt(i, 3).toString())
                        
                );
            }
            
        }
        return null;
    }
    
