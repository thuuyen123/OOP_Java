    private int getMin(String[] list){
        int min = Integer.parseInt(list[0]);
        for (int i = 0; i < list.length; i++) {
            int number = Integer.parseInt(list[i]);
            if(number<min) min = number;
        }
        return min;
    }
    private int getMax(String[] list){
        int max = Integer.parseInt(list[0]);
        for (int i = 0; i < list.length; i++) {
            int number = Integer.parseInt(list[i]);
            if(max>number) max = number;
        }
        return max;
    }
    private int getSum(String[] list){
        int sum =0;
        for (int i = 0; i < list.length; i++) {
            int number = Integer.parseInt(list[i]);
            sum += number;
            
        }
        return sum;
    }
    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        String content = q4.getContent().trim();
        String list[] = content.split(" ");
        
        List<String> selectedValues = jList1.getSelectedValuesList();
        String text = "Calculating by ";
        for (int i = 0; i < selectedValues.size(); i++) {
           text += selectedValues.get(i)+", ";
        }
        text += "\n";
        if(jRadioButton1.isSelected()){
            text += "Sum = " + getSum(list) + "\n";
        }
        if(jRadioButton2.isSelected()){
            text += "Max = "+getMax(list) +"\n";
        }
        if(jRadioButton3.isSelected()){
            text += "Min =" +getMin(list) +"\n";
        }
        q4.setContent(text);
        dispose();
    }//GEN-LAST:event_jButton1ActionPerformed

